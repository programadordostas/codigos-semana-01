# Operadores Logicos

x , y  = 20 , 15

res = x > y
print("x es mayor que y?",res)

res = x >= y
print("x es mayor o igual que y?",res)

res = x < y
print("x es menor que y?",res)

res = x <= y
print("x es menor o igual que y?",res)

res = x != y
print("Son diferentes?",res)

res = x == y
print("Son iguales?",res)

